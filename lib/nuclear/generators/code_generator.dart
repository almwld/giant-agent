class NuclearAgent:
    \"\"\"الوكيل النووي المدمر - الرئيسي\"\"\"
    
    def __init__(self):
        self.config = Config()
        self.processor = DataProcessor(self.config)
        self.analyzer = AdvancedAnalyzer()
        self.reporter = ReportGenerator()
        self.tasks: List[Task] = []
    
    async def execute_task(self, task_name: str, data: Any) -> Task:
        \"\"\"تنفيذ مهمة\"\"\"
        task = Task(id=str(len(self.tasks) + 1), name=task_name)
        task.status = TaskStatus.RUNNING
        self.tasks.append(task)
        
        try:
            if task_name == 'analyze':
                result = self.analyzer.analyze_text(data)
            elif task_name == 'process':
                self.processor.data = data
                result = self.processor.process()
            else:
                result = {'message': f'Task {task_name} executed'}
            
            task.result = result
            task.status = TaskStatus.COMPLETED
        except Exception as e:
            task.error = str(e)
            task.status = TaskStatus.FAILED
        
        task.completed_at = datetime.now()
        return task
    
    def get_statistics(self) -> Dict[str, Any]:
        \"\"\"الحصول على الإحصائيات\"\"\"
        completed = [t for t in self.tasks if t.status == TaskStatus.COMPLETED]
        failed = [t for t in self.tasks if t.status == TaskStatus.FAILED]
        
        return {
            'total_tasks': len(self.tasks),
            'completed': len(completed),
            'failed': len(failed),
            'success_rate': (len(completed) / len(self.tasks) * 100) if self.tasks else 0,
            'config': {
                'name': self.config.name,
                'version': self.config.version,
            }
        }

