#!/usr/bin/env python3
# -*- coding: utf-8 -*-
\"\"\"
MASS TEXT PROCESSOR - Handle 1000+ texts simultaneously
$description
Generated by Nuclear Giant Agent
\"\"\"

import json
import asyncio
import concurrent.futures
from typing import List, Dict, Any
from datetime import datetime
from dataclasses import dataclass
import threading
from collections import Counter

@dataclass
class ProcessedText:
    original: str
    length: int
    word_count: int
    sentiment: str
    keywords: List[str]
    processed_at: str

class MassTextProcessor:
    \"\"\"معالج النصوص الضخم - 10000+ نص في نفس الوقت\"\"\"
    
    def __init__(self, max_workers: int = 100):
        self.max_workers = max_workers
        self.results: List[ProcessedText] = []
        self.lock = threading.Lock()
    
    def process_text(self, text: str, index: int) -> ProcessedText:
        \"\"\"معالجة نص واحد\"\"\"
        words = text.split()
        sentiment = self._analyze_sentiment(text)
        keywords = self._extract_keywords(text)
        
        return ProcessedText(
            original=text[:200] + '...' if len(text) > 200 else text,
            length=len(text),
            word_count=len(words),
            sentiment=sentiment,
            keywords=keywords[:10],
            processed_at=datetime.now().isoformat()
        )
    def process_batch(self, texts: List[str]) -> List[ProcessedText]:
        \"\"\"معالجة مجموعة من النصوص باستخدام تعدد المهام\"\"\"
        results = []
        with concurrent.futures.ThreadPoolExecutor(max_workers=self.max_workers) as executor:
            futures = {executor.submit(self.process_text, text, i): i for i, text in enumerate(texts)}
            for future in concurrent.futures.as_completed(futures):
                results.append(future.result())
        return results
    
    async def process_async_batch(self, texts: List[str]) -> List[ProcessedText]:
        \"\"\"معالجة غير متزامنة لمجموعة كبيرة من النصوص\"\"\"
        return await asyncio.to_thread(self.process_batch, texts)
    
    def _analyze_sentiment(self, text: str) -> str:
        positive = ['good', 'great', 'excellent', 'amazing', 'wonderful', 'perfect']
        negative = ['bad', 'terrible', 'awful', 'horrible', 'worst', 'poor']
        
        text_lower = text.lower()
        pos_count = sum(1 for w in positive if w in text_lower)
        neg_count = sum(1 for w in negative if w in text_lower)
        
        if pos_count > neg_count:
            return 'POSITIVE'
        elif neg_count > pos_count:
            return 'NEGATIVE'
        return 'NEUTRAL'
    
    def _extract_keywords(self, text: str) -> List[str]:
        words = text.lower().split()
        stop_words = {'the', 'and', 'to', 'of', 'a', 'in', 'for', 'on', 'with', 'is', 'that', 'this'}
        keywords = [w for w in words if w not in stop_words and len(w) > 3]
        return list(dict.fromkeys(keywords))
    
    def get_statistics(self) -> Dict[str, Any]:
        \"\"\"الحصول على إحصائيات المعالجة\"\"\"
        if not self.results:
            return {'error': 'No results yet'}
        
        total_chars = sum(r.length for r in self.results)
        total_words = sum(r.word_count for r in self.results)
        sentiments = Counter(r.sentiment for r in self.results)
        
        return {
            'total_processed': len(self.results),
            'total_chars': total_chars,
            'total_words': total_words,
            'avg_chars_per_text': total_chars / len(self.results),
            'avg_words_per_text': total_words / len(self.results),
            'sentiment_distribution': dict(sentiments),
            'unique_keywords': len(set(kw for r in self.results for kw in r.keywords)),
        }
    
    def export_results(self, filepath: str) -> None:
        \"\"\"تصدير النتائج إلى ملف\"\"\"
        data = {
            'processed_at': datetime.now().isoformat(),
            'total_processed': len(self.results),
            'statistics': self.get_statistics(),
            'results': [
                {
                    'text': r.original,
                    'length': r.length,
                    'word_count': r.word_count,
                    'sentiment': r.sentiment,
                    'keywords': r.keywords,
                    'processed_at': r.processed_at
                }
                for r in self.results
            ]
        }
        with open(filepath, 'w', encoding='utf-8') as f:
            json.dump(data, f, indent=2, ensure_ascii=False)

